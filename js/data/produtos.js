export function normalize(value) {
  return value
    .toString()
    .trim()
    .toLowerCase()
    .normalize('NFD')
    .replace(/[^a-z0-9\s-]/g, '');
}

export function produtoPorId(id) {
  return produtos.find((produto) => produto.id === id) || null;
}

export function produtosPorCategoria(categoria) {
  return produtos.filter((produto) => normalize(produto.categoria) === normalize(categoria));
}

export function produtosEmDestaque() {
  return produtos.filter((produto) => produto.destaque);
}

export function formatarKwanza(valor) {
  return `${Number(valor || 0).toLocaleString('de-DE')} Kz`;
}

export function formatarPrecoProduto(produto) {
  const preco = formatarKwanza(produto?.preco || 0);
  return produto?.unidade ? `${preco}/${produto.unidade}` : preco;
}

export function buscarProdutos(query = '') {
  const termo = normalize(query);

  if (!termo) {
    return produtos.slice(0, 6);
  }

  return produtos.filter((produto) => {
    const haystack = [
      produto.nome,
      produto.categoria,
      produto.descricao,
      produto.preco,
      produto.unidade || '',
      ...(produto.palavras || [])
    ].join(' ');

    return normalize(haystack).includes(termo);
  }).slice(0, 8);
}
